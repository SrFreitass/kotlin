fun main() {
    // VAR - Mutável
    // VAL - Imutável

    // Sem inferição
    val countImutable: Int = 0;

    // Com inferição
    val countImutable2 = 0;

    println(countImutable);
    println(countImutable2);

    // Interpolação

    println("count 1: $countImutable \ncount 2: $countImutable2");
}
